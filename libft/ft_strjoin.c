#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*ret;
	size_t	len;
	size_t	s1_len;
	size_t	s2_len;

	if (!s1 || !s2)
		return (NULL);
	s1_len = ft_strlen(s1);
	s2_len = ft_strlen(s2);
	len = s1_len + s2_len;
	ret = malloc(sizeof(char) * (len + 1));
	if (ret == NULL)
		return (ret);
	ft_strlcpy(ret, s1, s1_len + 1);
	ft_strlcat(ret, s2, len);
	return (ret);
}
